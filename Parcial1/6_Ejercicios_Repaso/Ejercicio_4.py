#Solicitar 2 numeros al usuario
# y realizar todas las operaciones
# basicas de una calculadora (+,-,*,/)
# y mostrar por pantalla el resultado

#Entrada de datos
num_1=float(input("Ingrese el primer numero: "))
num_2=float(input("Ingrese el segundo numero: "))

#Proceso
resul_sum=num_1+num_2
resul_res=num_1-num_2
resul_mul=num_1*num_2
resul_div=num_1/num_2\

#Salida
print(f"{num_1} + {num_2} = {resul_sum}")
print(f"{num_1} - {num_2} = {resul_res}")
print(f"{num_1} * {num_2} = {resul_mul}")
print(f"{num_1} / {num_2} = {resul_div}")